function createPlaceholderJPEG(width, height, title) {
  // Create a valid minimal JPEG file
  // This is a minimal valid JPEG that displays a color
  // For a proper solution, you'd use a library like `sharp` or `jimp`

  // Fallback: Create using canvas if available, otherwise use a pre-made buffer
  try {
    const Canvas = require('canvas');
    const canvas = Canvas.createCanvas(width, height);
    const ctx = canvas.getContext('2d');

    // Background gradient
    const gradient = ctx.createLinearGradient(0, 0, width, height);
    gradient.addColorStop(0, '#1D4871');
    gradient.addColorStop(1, '#2367EE');
    ctx.fillStyle = gradient;
    ctx.fillRect(0, 0, width, height);

    // Semi-transparent overlay
    ctx.fillStyle = 'rgba(255, 255, 255, 0.05)';
    ctx.fillRect(0, 0, width, height);

    // Title text
    ctx.fillStyle = 'white';
    ctx.font = 'bold 52px Arial';
    ctx.textAlign = 'center';
    ctx.textBaseline = 'middle';
    ctx.fillText(title, width / 2, height / 2 - 40);

    // Subtitle
    ctx.fillStyle = 'rgba(255, 255, 255, 0.8)';
    ctx.font = '24px Arial';
    ctx.fillText('Sayso Blog', width / 2, height / 2 + 40);

    // Accent circles
    ctx.fillStyle = 'rgba(255, 222, 89, 0.8)';
    ctx.beginPath();
    ctx.arc(150, height - 80, 40, 0, Math.PI * 2);
    ctx.fill();

    ctx.fillStyle = 'rgba(255, 222, 89, 0.6)';
    ctx.beginPath();
    ctx.arc(width - 150, 80, 60, 0, Math.PI * 2);
    ctx.fill();

    return canvas.toBuffer('image/jpeg', { quality: 0.8 });
  } catch (e) {
    console.log(`‚ö† Canvas not available, using minimal placeholder for: ${title}`);
    // Return a minimal valid JPEG as fallback
    return createMinimalJPEG();
  }
}

